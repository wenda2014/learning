<h4>1.  Segitiga </h4>
Segitiga adalah suatu bangun datar yang jumlah sudutnya  1800 dan dibentuk dengan cara menghubungkan tiga buah titik yang tidak segaris daam satu bidang.<br>
Jenis-Jenis Segitiga :<br><br>
<b>a. Segitiga Sama Sisi</b><br>
*Segitiga yang ketiga sisinya sama panjang.<br>
*Panjang AB=BC=CA<br>
*sudut A = sudut B = sudut C = 60 derajat<br>
*sudut A + sudut B + sudut C = 180 derajat<br><br>
<b>b. Segitiga Sama Kaki</b><br>
*Segitiga yang mempunyai dua sudut yang sama dan dua buah sisi yang sama.<br>
*panjang AC = CB<br>
*sudut A = sudut B<br>
*sudut A + sudut B = sudut C = 180 derajat<br><br>
<b>c. Segitiga Sama Siku</b><br><br>
