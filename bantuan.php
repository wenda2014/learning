<h3 align="center">Aplikasi Pembelajaran UN SD</h3>
<b>Aplikasi Pembelajaran UN SD</b> adalah aplikasi yang bisa digunakan untuk membantu proses belajar siswa SD untuk menghadapi UN (Ujian Nasianal) selain di sekolahan. Pada aplikasi ini terdapat tiga mata pelajaran yaitu MTK,IPA,Bahasa Indonesia.
<h3>Daftar Isi Aplikasi</h3>
Pada aplikasi pembelajaran ini terdapat beberapa isi atau fitur yang ditampilkan. Berikut Fitur-fiturnya :<br>
<b>- Materi</b><br>
<b>- Latihan Soal</b><br>
<b>- Bantuan</b><br>
<b>- Tentang</b><br><br>
<b>Cara penggunaan aplikasi</b><br>
1. User (membuka) -> Aplikasi -> (tampil) Menu Utama<br>
2. Menu Utama :<br></b>
<b>---Pilih salah satu Menu Utama---</b><br>
Materi<br>
Latihan Soal<br>
Bantuan <br>
Tentang <br>
3. Keluar<br><br>
<b>Fitur Aplikasi</b><br><br>
<b>=== Materi ===</b><br>
Tampil Menu Utama -> Pilih Menu Materi -> Pilih salah satu Pelajaran UN -> Pilih Bab Materi -> Pilih Sub Menu Materi -> Tampil Detail Materi.<br>
Pada Menu Materi terdapat tiga mata pelajaran UN yaitu Matematika, Bahasa Indonesia, IPA. Pada masing-masing pelajaran terdapat 2 bab, sub menu materi, dan detail materi. yang bisa membantu siswa untuk proses belajar.<br><br>
<b>=== Latihan Soal ===</b><br>
Tampil Menu Utama -> Pilih Menu Latihan Soal -> Pilih salah satu Pelajaran UN -> Pilih Sub Latihan Soal -> Tampil Detail Latihan Soal.<br>
Pada Menu Latihan Soal terdapat tiga mata Pelajaran UN yaitu Matematika, Bahasa Indonesia, dan IPA. Pada masing-masing mata pelajaran UN terdapat latihan soal yang harus dijawab user atau pengguna aplikasi setelah selesai menjawab user akan mengetahui nilai yang sudah sudah dijawabnya, berapa jawaban yang benar dan berapa jawaban yang salah, dan tentu saja secore juga ditampilkan.<br><br>
<b>=== Bantuan ===</b><br>
Tampil Menu Utama -> Pilih Menu Bantuan -> Tampil detail Menu Bantuan.<br>
Pada Menu Bantuan berisi tentang bgaimana cara memakai atau menggunakan Aplikasi Pembelajaran UN.<br><br>
<b>=== Tentang ===</b><br>
Tampil Menu Utama -> Pilih Menu Tentang -> Tampil detail Menu Tentang si pembuat aplikasi.
