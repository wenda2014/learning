<b>1. Hubungan Antarmakhluk Hidup </b><br>
Setiap makhluk hidup tidak bisa hidup sendiri, tetapi saling membutuhkan satu sama lainnya. Hubungan khusus antara dua makhluk hidup yang berlainan jenis dinamakan <b>simbiosis</b><br><br>
<b>Tiga Macam simbiosis :</b><br>
a. <b>Simbiosis mutualisme</b>, yaitu hubungan yang saling menguntungkan antara dua jenis makhluk hidup.Contoh:<br>
- Burung jalak dengan kerbau (jalak mendapatkan makanan, kerbau, bersih dari kutu.<br>
- jamur dengan lumut kerak.<br>
- Bunga sepatu dengan lebah (bunga sepatu  terbantu penyerbukannya, lebah mendapatkan makanan.<br><br>
b. <b>Simbiosis parasitisme</b>, yaitu hubungan antara dua jenis makhluk hidup di mana salah satu jenis akan diutungkan sedangkan yang satu akan dirugikan.Contoh:<br>
- Tali Putri dengan tumbuhan inangnya (tali putri akan muncuri makanan/hasil fotosintesis tumbuhan yng    ditinggalinya.<br>
- Benalu dengan tumbuhan inangnya.<br><br>
c. <b>Simbiosis komensalisme</b>, hubungan antara dua jenis makhluk hidup di mana salah satu jenis akan diuntungkan sedangkan jenis yang lainya tidak diuntungkan dan tidak dirugikan.Contoh:<br>
- Anggrek dengan tumbuhan inangnya.<br>
- Sirih dengan tumbuhan inangnya<br><br>
<b>2. Rantai Makanan.</b><br>
<b>Rantai Makanan</b> merupakan hubungan makan dan dimakan beberapa makhluk hidup dengan urutan tertentu. Rantai makanan diawali dengan keberadaan tumbuhan sebagai penghasil makanan bagi makhluk hidup lainya, sehingga disebut dengan <b>produsen</b>. Hewan yang memakan tubuhan disebut dengan <b>konsumen tingakat pertama</b>(konsumen I). Selanjutnya hewan yang memakan hewan tingkat pertama disebut <b>konsumen tingkat kedua</b> (konsumen II), dan seterusnya. Komponen yang juga penting dalam rantai makanan adalah <b>pengurai</b> yaitu makhluk hidup yang menguraikan kembali zat-zat pada makhluk hidup yang telah mati, contohnya jamur  dan bakteri.<br><br>
