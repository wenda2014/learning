<b>3. Peran/Manfaat Tumbuhan Bagi Makhluk Hidup disekitarnya</b><br>
Beberapa contoh peran tumbuhan bagi manusia antara lain:<br>
a. Sumber makanan utama: jagung, padi, gandum.<br>
b. Dijadikan buah-buahan: salak, pepaya, jeruk, dan lain-lain.<br>
