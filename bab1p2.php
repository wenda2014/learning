<b>Perkembang Biakan Hewan & Tumbuhan</b><br><br>
<b>Berkembang Biak</b> merupakan kemampuan menghasilakan keturunan/individu baru yang bertujuan melestarikan atau meneruskan kelangsungan hidup sejenisnya.<br><br>
 <b>1. Perkembangbiakan Hewan</b><br>
 Perkembangbiakan Hewan secara umum dapat terjadi memlaui 3 cara:<br>
<b> a. Bertelur (Ovipar)</b><br>
Perkembangan calon individu baru (embrio) di dalam telur hingga menetas. Telur menetas diluar tubuh induknya. Contohnya: ayam, ikan.<br>
<b>b. Melahirkan (vivipar)</b><br>
Perkembangan calon individu baru di dalam tubuh induknya, kemudian dilahirkan dalam betuk individu baru. Contohnya: singa, gajah.<br>
Ciri utama hewan vivipar antara lain:<br>
1) berdaun telinga<br>
2) bernapas menggunakan paru-paru<br>
3) memiliki puting susu<br>
4) tubuh berambut<br>
5) mengalami masa mengandung<br>
<b>c. Bertelur dan melahirkan (ovovivipar)</b><br>
Perkembangan calon individu baru didalam telur hingga menetas, akan tetapi telur menetas didalam tubuh induknya, sehingga individu baru akan dikeluarkan /dilahirkan bukan dalambentuk telor. Contohnya: hiu dan ular boa.<br><br>
<b>2. Perkembangbiakan Tumbuhan </b><br>
<b>a. Perkembangbiakan secara generatif</b><br>
Perkembangbiakan ini terjadi melalui pembuahan(peleburan kelamin jantan dan sel kelamin betina yang sebelumnya didahului dengan penyerbukan (bertemunya serbuk sari dan kepala putik).<br><br>
<b>b. Perkembangbiakan secara vegetatif</b><br>
Perkembangbiakan makhluk hidup tanpa adanya peleburan sel kelamin jantan dan betina. atau disebut aseksual.<br>
<b>1) Vegetatif alami</b> yaitu perkembangbiakan vegetatif tumbuhan tanpa ada campur tangan manusia. Bagian tumbuhan yang digunakan sebagai alat perkembangbiakan vegetatif alami meliputi, sepora, tunas, tunas adventif, umbi akar, umbi batang, umbi lapis, akar tinggal, geragih dan stolon<br>
<b>2) Vegetatif buatan</b> yaitu perkembangbiakn vegetatif tumbuhan melalui campur tangan manusia. Bentuk upaya perkembangbiakan dapat dilakukan dengan cara, menyetek, mencangkok, merunduk, kultur jaringan<br><br> 

