  <b>1. Perubahan Wujud Benda</b><br>
<b>a. Mencair</b><br>
Perubahan wujud benda dari padat menjadi cair, contoh : melelehnya es dan margarin ketika mendapat panas.<br>
<b>b. Menyublin</b><br>
Perubahan wujud benda dari padat menjadi gas, contoh: kapur barus yang menyublin.<br>
<b>c. Menguap</b><br>
Perubahan dari cair menjadi gas, contoh: pakaian yang basah menjadi kering dan air yang mendidih ketika dipanaskan.<br>
<b>d. Membeku</b><br>
Perubahan wujud dari cair menjadi padat, contoh: es yang membeku dan pembuatan agar-agar.<br>
<b>e. mengembun</b><br>
Perubahan wujud benda dari gas menjadi cair, contoh:tetesan embun di daun.<br>
<b>f. Mengkristal</b><br>
perubahan wujud benda dari gas menjadi padat, contoh: gas belerang di kawah gunung yang membentuk kristal belerang.<br><br>
<b>2. Pengaruh Perubahan Suhu Terhadap Benda Dalam Kehidupan Sehari-hari</b><br><br>
Pengaruh suhu terhadap benda dijelaskan sebagai berikut.<br>
<b>a. Pemuaian</b><br>
Pada benda-benda tertentu, suhu yang panas mengakibatkan pemuaian, sedangkan suhu yang dinggin dapat menyebabkan penyusutan. Perubahan benda akibat suhu diterapkan dalam teknologi pemasangan rel kerta api dan kawat listrik.<br>
- Rel kerta api dibuat renggang. Rel yang terbuat dari baja akan memuai jika kena panas , untuk itu disediakan  ruan pada sambungaan antar rel.<br>
<b>b. Pembakaran</b><br>
Proses pembakaran pada suhu tinggi dapat menyebabkan perubahan bentuk pada benda.<br>
Contoh:<br>
- Logam akan mudah dibentuk bila dibakar terlebih dahulu.<br>
- Kayu dapat berubah bentuk menjadi arang bila dibakar.<br>
<b>c. Penguapan atau pengeringan</b><br>
Zat cair akan mengalami penguapan pengeringan bila dipanaskan.<br>
Contoh:<br>
- Pakaian yang dijemur menjadi kering karena air yang terkandung akan menguap.<br>
- Pembuatan garam melalui penguapan air laut.<br><br>
<b>3. Macam-macam perpindahan kalor</b><br>
<b>a. Konduksi</b><br>
Perpindahan panas pada suatu benda melalui zat perantara, tanpa disertai perpindahan zat tersebut.<br>
Contoh:<br>
- Perpindahan panas pada sendok logam.<br>
<b>b. Konveksi</b><br>
Perpindahan pada suatu benda melalui zat peranta diserta perpindahan zat tersebut.<br>
Contoh:<br>
adanya gerakan naik turun kacang hijau dalam air yang mendidih saat kita merebus kacang hijau serta terjadinya angin darat dan angin laut.<br>
<b>c. Radiasi</b><br>
Perpindahan panas tanpa melalui zat perantara.<br>
Contoh:<br>
Pancaran panas matahari sampai ke bumi serta tubuh terasa hangat ketika berada didekat api unggun.<br>
 
