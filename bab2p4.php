<b>1. Jenis-Jenis Tanah</b><br>
<b>a. Tanah Humus</b><br>
Merupakan tanah yang paling subur karena mengandung banyak humus. Berwarna gelap dan baik untuk pertanian.<br>
<b>b. Tanah Gambut</b><br>
Terbentuk di daerah asam. Bersifat asam, berwarna gelap, bertekstur lunak dan basah, kurang subur, sehingga kurang cocok untuk pertanian.<br>
<b>c. Tanah Lampung/Tanah Liat</b><br>
Terdiri dari butiran-butiran halus yang lengket satu sama lain. Banyak digunakan untuk membuat kerajinan gerabah karena mudah dibentuk.<br>
<b>d. Tanah Kaolin</b><br>
Tanah Kaolin adalah tanah liat yang mengandung mineral kaolinit. Bayak digunakan untuk membuat keramik.<br>
<b>e. Tanah Berpasir</b><br>
Mengandung butiran pasir yang sangat banyak, mudah dilalui air. Banyak Dimanfaatkan untuk membuat bangunan.<br>
<b>f. Tanah Vulkanik</b><br>
Merupakan tanah yang subur hasil letusan gunung berapi, mudah menyerap air. Banyak Mengandung zat hara sehingga baik untuk pertanian.<br>
<b>g. Tanah Berkapur</b><br>
Mengandung bebatuan, mudah dilalui air, sedikit mengandung zat hara sehingga kurang subur.<br><br>
<b>2. Jenis Sumber Daya Alam Dan Manffatnya </b><br>
<b>Sumber daya alam</b> adalah segala yang ada di alam dan dipergunakan oleh manusia untuk memenuhi kebutuhannya.<br><br>
<b>Pemanfaatan Sumber Daya alam hayati.</b><br>
<b>a. Bahan pangan</b><br>
1) Minyak goreng dari kelapa sawit.<br>
2) Roti dan gandum.<br>
3) Daging dari ayam, sapi, kambing.<br>
<b>b. Bahan sandang</b><br>
1) Kain wol dari serat rambut domba.<br>
2) Kain katun dari serat kapas.<br>
3) Kain sutra dari serat kepompong ulat sutra.<br>
<b>c. Peralatan rumah tangga</b><br>
1) Kertas dari kayu pinus.<br>
2) Meja, kursi, lemari, dan pintu dari kayu jati.<br>
<b>d. Obat Tradisonal</b><br>
1) Kunyit dan temulawak untuk menambah nafsu makan.<br>
2) Lidah buaya untuk sampo.<br><br>
<b> Pemanfaatan sumber daya alam nonhayati</b><br>
<b>a. Bahan Bagunan</b><br>
1) Batu bata dan genting dari tanah liat<br>
2) Batako dari pasir dan semen.<br>
<b>b. Bahan tambang logam</b><br>
1) Emas dan perak untuk perhiasan.<br>
2) Alumunium untuk peralatan dapur.<br>
3) Tembaga untuk bahan kawat dan kabel.<br>
<b>c. Bahan tambang nonlogam</b><br>
1) Gipsum untuk bahan cat tembok><br>
2) Asbes untuk bahan atap rumah.<br>
3) Belerang untuk bahan obat-obatan.<br>
