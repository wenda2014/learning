<b>1. Alat Pernapasan Manusia</b><br><br>
<img src="Image_Ipa/alatpernapasan.png" width="229" height="169"><br><br>
<b>Alat Pernapasan Manusia</b><br>
<b>a. Hidung</b><br>
<b>Hidung</b> adalah pernapasan pertama tempat masuknya udara. Udara yang masuk akan disaring oleh rambut hidung, kotoran hasil penyaringan akan lengket dan terkumpul oleh lendir, lendir juga berfungsi mengatur suhu dan kelembapan udara yang masuk tubuh.<br>
<b>b. Tenggorokan</b><br>
<b>Tenggorokan</b>
Setelah melewati hidung, udara masuk ke tenggorokan. Pada tenggorokan juga terdapat rambut halus yang berfungsi menyaring udara dari kotoran yang masih dapat lolos dari hidung. Ujung bawah tenggorokan bercabang menjadi dua bagian, yaitu bronkus kanan yang menuju paru-paru kanan dan bronkus kiri yang menuju paru-paru kiri.<br>
<b>c. Paru-paru</b><br>
<b.Paru-paru</b> dibungkus oleh selaput paru-paru yang disebut pleura. Bronkus juga akan bercabang lagi yang disebut dengan bronkiolus. Bronkiolus juga memiliki percabangan yang jumlahnya banyak dan tiap-tiap ujung cabang membentuk kantung berdinding tipis yang disebut alveolus. Alveolus berfungsi sebagai tempat pertukaran gas O2
 dan CO2.<br><br>
<b>2. Alat Pencernaan Manusia</b><br><br>
<img src="Image_Ipa/pencernaan.jpg" width="200" height="200"><br><br>
<b>Alat-alat pencernaan</b><br>
<b>a. Mulut</b><br>
pencernaan dalam mulut terjadi secara mekanis dan manusiawi.<br>
- Secara mekanis dengan mengunyang/mengolah makanan menggunakan gigi dan lidah.<br>
- Secara kimiawi menggunakan bantuan air ludah yang berfungsi mengubah zat tepung (amilum) menjadi zat gula sederhana.<br>
<b>b. Kerongkongan</b><br>
Terjadi gerak peristaltik dalam kerongkongan, yaitu gerakan mendorong makanan menuju lambung.<br>
<b>c. Lambung</b><br>
Pencernaan trjadi secara mekanis dan manusiawi.<br>
1) Secara mekanis dengan  mengaduk dan meremas makanan menggunakan otot dinding lambung.<br>
2) Secara kimiawi dengan menggunakan:<br>
- Asam klorida (HCI, membunuh kuman-kuman yang masuk bersama makanan.<br>
- Enzim pepsin, mengubah protein menjadi pepton.<br>
- Enzim renin, mengendapkan protein susu menjadi kasein.<br>
<b>d. Usus halus</b><br>
Didalam usus duabelas jari terjadi pencernaan menggunkan getah empedudan getah pankreas. Getah empedu akan membantu memecahkan lemak dan sebagai tempat pembuangan limbah tertentu dalam tubuh.<br>
<b>e. Usus besar</b><br>
Terjadi penyerapan air dan garam mineral, kemudian sisaanya dibusukkan dengan bantuan bakteri Escherichia coli.<br>
<b>f. Anus</b><br>
Sebagai tempat pengeluaran hasil pencernaan yang telah dibusukkan pada usus besar.<br>
