<b>Faktor prima suatu Bilangan</b> adalah bilangan-bilangan prima yang membagi habis bilangan tersebut.<b>Faktorisasi Prima</b> adalah bentuk perkalian faktor-faktor prima dari suatu bilangan.<br><br>
<b>A. KPK</b><br>
<b>Kelipatan persekutuan terkecil (KPK)</b> dari dua bilangan adalah kelipatan persekutuan bilangan- bilangan tersebut yang nilainya paling kecil.<br>
Cara menentuka KPK:<br>
* Setaip bilangan diuraikan menjadi bentuk perkalian faktor-faktor primanya.<br>
* KPK dapat ditentukan dengan mengalikan semua faktor yang ada.Jika ada faktor yang sama pilihlah yang paangkatnya besar<br><br>
Penyelesain masalah sehari-hari yang berkaitan dengan KPK dapat dilakukan dengan langka-langkah berikut.<br>
1. Pahami soal baik-baik. Untuk soal cerita yang melibatkan KPK biasanya terdapat kataa-kata"bersama-sama"atau"bersamaan".<br>
2. Tentukan faktorisasi dari angka-angka yang akan dicari KPK-nya.<br>
3. Carilah KPK dari bilangan tersebut.<br>
4. Carilah penyelesain dari soal tersebut.<br><br>
<b>B. FPB</b><br>
<b>Faktor Persekutuan Terbesar(FFB)</b> dari dua bilangan adalah faktor persekutuan bilangan-bilangan tersebut yang nilainya paling besar.<br>
Cara menentukan FPB:<br>
* Setiap bilangan diuraikan menjadi bentuk perkalian faktor-faktor primanya.<br>
* FPB dapat ditentukan dari perkalian faktor-faktor yang bersekutu, dengan pangkat terkecil.<br><br>
Penyelesaian masalah sehari-hari yang berkaitan dengan FPB dapat dilakukan langkah-langkah berikut:<br>
1. Pahami soal baik-baik. Untuk soal cerita yang melibatkan KPK biasanya terdapat kataa-kata"bersama-sama"atau"bersamaan".<br>
2. Tentukan faktorisasi dari angka-angka yang akan dicari FPB-nya.<br>
3. Carilah FPB dari bilangan tersebut.<br>
4. Carilah penyelesain dari soal tersebut.<br><br>
