<b>1. Mengartikan Kata Sulit Dalam Kalimat</b><br>
Arti kata biasa juga disebut dengan makna kata. Untuk mengartikan suatu kata dalam kalimat, kita perlu memahami jenis makna leksikal.<b>Makna Leksial</b> ialah makna kata yang timbul secara lepas tanpa ada kaitanya dengan dengan kata lainya<br><br>
<b>a. Makna Leksikal Kata Dasar </b><br>
<b>Contohnya</b><br>
-Rumah =bangunan untuk tempat tinggal manusia.<br>
-Makan =mengunyah menelan sesuatu<br><br>
<b>b. Makna Leksikal Kata berimbuhan</b><br>
<b>Contohnya</b><br>
-Menghasilkan sesuatu, contoh: menyayur, manusia.<br>
-Melakukan pekerjaan, contoh: menulis, memasak.<br>
-Melakukan Pekerjaan dengan alat, contoh: mengecaat, mencangkul.<br>
-Memberi atau membubuhi, contoh: mengapur, menambal.<br>
-Membuat jadi, contoh: mengotori, menghitamkan.<br>
-Mengambil, contoh: membului, menguliti.<br>
-Mengeluarkan bunyi, contoh: mendesis.<br>
-Mengeluarkan atau menapilkan, contoh:melompat, menangis<br>
-Menjadi, contoh: meninggi, meluas.<br>
-Berlaku seperti, contoh: meraja, membio.<br><br>
