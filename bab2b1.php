<b>1. Pribahasa</b><br>
<b>Pribahasa</b> adalah bahasa berkias yang berupa kalimat yang atau kelompok kata. Pribahsa digunakan untuk mengiasakan maksud tertentu. Oleh karena itu, pribahasa juga disebut sebagai bahasa kiasan.<br>
<b>contohnya</b><br>
