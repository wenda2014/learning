b. Buah jatuh tak akan jauh dari pohonnya = sifat anak tidak jauh dari orang tuanya.<br><br>
<b>2. Ungkapan</b><br>
<b>Ungkapan</b> adalah gabungan kata yang menyatakan 
makna khusus. Ungkapan juga sering disebut dengan idiom. Ungkapan selalu menimbulkan makna baru. Makna baru yang ditimbulkan tersebut tidak memiliki hubungan dengan kata dasarnya, sehingga artinya terkadang menjadi kabur.<br><br>
Ada beberapa jenis ungkapan antara lain sebagai berikut.<br>
a. Ungkapan dengan bagian tubuh. Contoh: rendah hati(ramah).<br>
b. Ungkapan dengan indra. Contoh: kaki tangan (orang kepercayaan).<br>
c. Ungkapan dengan warna. Contoh: jago merah (api dalam kebakaran).<br>
d. Ungkapan dengan nma benda alam. Contoh: tanaj air (negeri kelahiran).<br>
e. Ungkapan dengan nama binatang. Contoh: kambing hitam (orang yang disalahkan).<br>
f. Ungkapan dengan bagian tumbuh-tumbuhhan. Contoh: bunga desa (wanita cantik).<br>
g. Ungkapan dengan kata bilangan. Contoh: setengah hati (tidak sungguh-sungguh).<br><br>

