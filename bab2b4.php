<b>b. Tanda Baca</b><br>
<b>Tanda baca</b> adalah tanda yang dipakai dalam ejaan.<br>
<b>* Tanda Titik (.)</b><br>
Dipakai untuk memsihkan angka jam, menit,dan detik, yang menunjukan waktu.<br>
<b>Contoh :</b><br>
1.35.20 (pukul 1 lewat 35 menit 20 detik)<br>
<b>* Tanda Koma (,)</b><br>
Dipakai diantara (i) nama dan alamat, (ii) bagian-bagian alamat, (iii) tempat dan tanggal yang ditulis secara berurutan.<br>
<b>Contoh :<br></b>
Sdr. Kurnia, Jalan Sagu Merah, Bogor, Indonesia.<br>
<b>* Tanda Titik Dua (:)</b><br>
Sesudah kata yang memperlukan pemerian.<br>
<b>Contoh :</b><br>
Hari :Senin, 7 Oktober 2013<br>
<b>* Tanda Garis Miring (/)</b><br>
Dipakai dalam nomor surat dan nomor pada alamat surat dan penandaan masa satu tahun yang terbagi dalam dua tahun penanggalan.<br>
<b>Contoh :</b><br>
No.9/PK/2012<br>
Jalan Kridosono IV/10<br>
tahun anggaran 2011/2012<br><br>
<b>2. Surat Resmi</b><br>
<b>Surat Resmi</b> adalah surat yang digunakan untuk kepentingan resmi, baik untuk kepentingan perseorangan maupun organisasi. Contoh surat resmi adalah surat undangan, surat edaran, dan surat pemberitahuan.<br>
<img src="Walpaper/Surat Resmi.jpg" width="300" height="320"><br>Ciri-ciri surat resmi adalah sebagai berikut :<br>
* Menggunakan kepala/kop surat bila dikeluarkan oleh organisasi.<br>
* Ada nomor surat, lampiran, dan perihal.<br>
* Menggunakan salam pembuka dan penutup.<br>
* Menggunakan ragam bahasa resmi.<br>
* Menyertakan cap atau stempel dari lembaga resmi.<br>
* Ada aturan format yang baku.<br>

