<b>1.  Laporan</b><br>
Pokok-pokok isi laporan merupakan hal-hal pentingyang disampaikan atau dilaporkan. Oleh kerana isi laporan berfungsi menginformasikan hal penting kepada pembaca, maka laporan harus ditulis dengan bahasa yang baik dan benar. Tujuanya agar para pembaca tidak salah mengerti dengan laporan yang ingin disampaikan tersebut. Caranya adalah dengan memperhatikan setiap struktur kalimat dasar dalam laporan.<br><br>
<b>2. Struktur Kalimat</b><br>
Struktur kalimat dasar diisi oleh unsur-unsur yang sifatnya tetap. Unsur-unsur tersebut berkedudukan sebagai fungsi dalam kalimat. Unsur-unsur tersebut adalah sebagai berikut adalah sebagia berikut.<br><br><b>a. Subjek</b> adalah unsur yang berfunsi sebagai pokok pembicara suatu kalimat.<br>
Contoh: <b>Susi</b> belajar di kamar.<br>
<b>b. Predikat</b> adalah unsur kalimat yang berfungsi untuk menjelaskan subjek.<br>
Contoh: Ardan <b>membuat</b> jus mangga.<br>
<b>c. Objek</b> adalah unsur kalimat yang hanya terdapat pada kalimat aktif.<br>
Contoh: Riska membawa <b>tas merah</b>.<br>
<b>d. Keterangan</b> merupakan unsur tambahan dalam sebuah kalimat.<br>
Contoh: <b>Besok</b> saya akan bersepeda <b>ditaman</b>.<br>
Sebuah kalimat yang baik selalu memiliki urutan fungsi Subjek-Predikat-Objek (S-P-O). Funsi keterangan dapat diletakan di mana saja. Sebab fungsi keterangan merupakan unsur tambahan. Misalnya S-P-O-K; S-P-K-O; S-K-P-O; atau K-S-P-O.<br><br>
