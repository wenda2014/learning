<b>Paragraf</b> adalah kumpulan satu kalimat utama dan beberapa kalimat penjelasan yang membentuk satu kesatuan pikiran. Jadi, kesatuan pikiran dalam paragraf itu dikembangkan melalui kalimat utamadan kalimat penjelas.<br><br>
<b>Kalimat Utama</b> merupakan gagasan pokok suatu paragraf. <b> Kalimat penjelasan</b> merupakan kalimat-kalimat yang sifatnya mendukung kalimat utama<br>
<b>Kalimat penjelas</b> adalah gagasan yang perannya menjelaskan gagasan utama. Biasanya berisikan :<br>
a. Contoh-contoh<br>
b. Kutipan-kutipan<br>
c. Uraian-urain kecil<br>
d. Peristiwa ilustrasi<br><br>
<b>Ciri-ciri kalimat utama adalah:</b><br>
a. Kalimat utama berisi gagasan pokok paragraf.<br>
b. Kalimat utama dapat terletak di awal atau akhir paragraf.<br>
c. Kalimat utama berupa pernyataan umum yang dirinci pada kalimat-kalimat lain.<br>
d. Kalimat Utama tidak mengandung kata kunci seperti: itu, tersebut, -nya.<br>
e. Kalimat utama biasanya mengandung kata kunci seprti : yang terpisah, adalah, pada prinsipnya.<br>
