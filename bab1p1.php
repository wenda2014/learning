<b>1. Ciri-Ciri Makhluk Hidup</b><br><br>
<b>A. Ciri-ciri Khusus Hewan</b><br>
* Struktur kaki pada tokek dan cecak memiliki lapisan yang lengket dan bentuk yang berulir untuk menempel dan berjalan di dinding maupun pohon<br>
* Bebek dan angsa memiliki kaki yang berselaput untuk berenang. Selain itu juga memiliki kelenjar yang menghasilkan minyak yang berfungsi untuk menjaga bulu tetap kering walaupun lama berendam di air<br>
* Bunglon memiliki kemampuaan untuk mengubaah warna tubuhnya sesuai dengan linkungan. Kemampuan ini disebut <b>mimikri</b>, berfungsi untuk melindungi diri dari musuhnya.<br><br>
<b>B. Ciri-Ciri Khusus Tumbuhan</b><br>
* Daun pada tumbuhan putri malu akan layu atau menutup ketika disentuh.<br>
* Kaktus memiliki lapisan daun yang tebal dan kecil seprti duri dilapisi oleh lignin untuk mengurangi penguapan air.<br>
* Teratai memiliki bentuk daun yang lebar untuk memperbesar penguapan air.<br>
* Kancang panjang memiliki sulur yang berguna untuk membantu menopang tubuh.<br>
* Tumbuhan Venus, daunya termodifikasi berbentuk engsel dan berbulu untuk menangkap serangga.<br><br>
<b>2. Dasar Pengelompokan Makhluk Hidup</b><br><br>
<b>A. Pengelompokan Hewan</b><br>
<b>1) Berdasarkan Tempat Hidupnya.</b><br>
a. Hewan yang hidup didarat, misalnya: ayam, bebek & kucing.<br>
b. Hewan yang hidup di air, misalnya: ikan, cumi-cumi & bintang laut.<br>
c. Hewan yang hidup diair & di darat (amfibi), misalnya: katak & salamander.<br><br>
<b>2) Berdasarkan perkembangbiaknya.</b><br>
a. <b>Ovivar</b> merupakan hewan yang berkembang biak dengan bertelur, misalnya: ayam, bebek, dan ular<br>
b. <b>Vivipar</b> merupakan hewan yang berkembang biak dengan beranak, misalnya badak, kerbau, dan harimau.<br>
c. <b>Ovovivar</b> merupakan hewan yang berkembang biak dengan bertelur dan melahirkan, misalnya hiu, kadal, dan beberapa jenis ular.<br><br>
<b>3) Berdasarkan jenis makananya.</b><br>
a. <b>Herbivora</b> merupakan hewan pemakan tumbuhan, misalnya kambing, jerapah, dan domba.<br>
b. <b>Karnivora</b> merupakan hewan pemakan daging, misalnya burung elang, singa, dan buaya.<br>
c. <b>Omnivora</b> merupakan hewan pemakan daging dan tumbuhan, misalnya ayam dan itik.<br><br>
<b>B. Pengelompokan Tumbuhan</b><br>
<b>1. Berdasarkan Tempat Hidupnya.</b><br>
a. Tumbuhan yang hidup didarat, misalnya: aksia, pepaya, dan beringin.<br>
b. Tumbuhan yang hidup di air, misalnya: teratai, eceng gondok, dan rumput laut.<br>
c. Tumbuhan yang hidupnya pada tumbuhan lain, misalnya: benalu, & anggrek.<br><br>
<b>2. Berdasarkan bentuk tulang daunnya.</b><br>
a. Tumbuhan bertulang daun menyirip, misalnya: mangga, rambutan,dan jambu.<br>
b. Tumbuhan bertulang daun sejajar, misalnya: padi, jagung & tebu.<br>
c. Tumbuhan bertulang daun menjari, misalnya: ketela, dan pepaya.<br>
d. Tumbuhan bertulang daun melengkung, misalnya waru & sirih.<br><br>
<b>3. Berdasarkan Bentuk akarnya</b><br>
a. Tumbuhan berakar tunggang, misalnya: bayam, jambu, mangga dan cabai.<br>
b. Tumbuhan Berakar Serabut, misalnya: tebu, jagung, dan kelapa.<br><br>
<b>C. Manfaat Hewan dan Tumbuhan bagi Kehidupan</b><br>
<b>1. Manfaat Hewan</b><br>
<b>a. Penghasil makanan:</b><br>
1) Ayam, Bebek dan Burung dapat dimanfaatkan daging & telornya.<br>
2) Sapi, Kambing, dan kuda dapat dimanfaatka daging & susunya.<br>
<b>b. Alat transportasi:</b> sapi, kerbau, kuda, keledai, daan unta.<br>
<b>c. Penghasil bahan sandang/Pakaian:</b><br>
1) Bulu Domba dapat dimanfaatkan sebagai kain wol.<br>2) Kulit hewan buaya dan ular dapat dimanfaatkan sebagai jaket atau mantel.<br><br>
<b>2. Manfaat Tumbuhan</b><br>
1) <b>Penghasil makanan:</b> kacang tanah, padi, dan jagung.<br>
2. <b>Penghasil bahan sandang:</b> kapas dan rami.<br>
3. <b>Sebagai bahan bangunan:</b> kayu jati, sengon laut, kelapa.<br>
4. <b>Kerajinan tangan:</b> eceng gondok dan pandan.<br>

