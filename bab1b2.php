<b>1. Menentukan Tokoh Utama dalam Drama</b><br>
<b>Tokoh</b> dalam drama adalah orang yang merupakan pelaku cerita.<br>

Menurut kedudukannya dalam cerita, tokoh dibagi menjadi tiga, yaitu:<br>
a. Tokoh Utama adalah tokoh yang menjadi pusat perhatian dan paling banyak sksinya.<br>
b. Tokoh pendukung adalah tokoh yang menciptakan situasi untuk memancing konflik/masalah.<br>
c. Tokoh figuran adalah tokoh yang tamoil tanpa dialog.<br>
Penokohan merupakan pemberian watak atau karakter pada masing-masing  pelaku dalam sebuah cerita.<br><br>
<b>2. Menetukan Latar Dalam Drama</b><br>
<b>Latar</b> adalah segla sesuatu yang mengacu pada keterangan mengenai waktu, ruang, serta suasana peristiwa. Latar drama suatu pementasan biasanya dibuat sebagai panggung yang dihiasi dengan dekorasi, seni lukis, tata panggung, seni patung, tata cahaya dan tata usaha.<br><br>
<b>3. Menentukan Amanat Dalam Drama</b><br>
<b>Amanat</b> adalah pesan yang ingin disampaikan penulis cerita kepada penonton drama 
lewat dialog maupun tingkah laku tokoh.<b>Dialog</b> adalah percakapan antara dua tokoh atau lebih.Dialog dapat menggambarkan karakter tokoh serta konflik/masalah yang timbul di dalamnya.<br>
