<b>1. Energi</b><br>
Energi adalah kemampuan untuk melakukan usaha.<br>
<b>Bentuk-bentuk energi</b><br>
1)<b> Energi Panas</b> adalah energi dalam bentuk panas.<br>
2)<b> Energi Bunyi</b> adalah energi yang dimilki oleh bunyi.<br>
3)<b> Energi listrik</b> adalah energi yang dimiliki arus listrik.<br>
4)<b> Energi kimia</b> adalah energi yang ditimbulkan akibat reaksi kimia.<br>
5)<b> Energi Cahaya</b> adalah energi yang dimiliki cahaya<br>
6)<b> Energi kinetik</b> adalah energi yang dimiliki cahaya.<br>
7)<b> Energi Potensial</b> adalah energi yang dimiliki benda diam dan dipengaruhi oleh ketinggian benda tersebut.<br><br>
<b>2. Perubahan Bentuk Energi </b><br>
1) Perubahan energi listrik menjadi energi panas.<br>
Contoh:<br>
Seterika listrik, solder yang digunakan, dan kompor listrik.<br>
2) Perubahan energi gerak menjadi energi bunyi.<br>
Contoh:<br>
