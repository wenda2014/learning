<b>1. Pengaruh kegiatan Manusia Terhadap Keseimbangan Lingkungan</b><br>
Keseimbanagn Lingkungan dapat terjadi ketika setiap komponen ekosistem tidak mengalami perubahan yang besar, dapat berupa jumlah komponen yang bertambah sangat banyak atau mungkin menghilangnya suatu komponen ekosistem. Contohnya jika suatu danau akan mengalami kekeringan, maka air dan ikan akan hilang, keadaan tersebut akan mempengaruhi keberadaan makhluk hidup lainnya.<br><br>
<b>2. Tujuan Adaptasi pada Hewan/</b><br>
<b>Adaptasi</b> merupakan suatu penyesuaian diri makhluk hidup terhadap lingkungan untuk mempertahankan hidupnya. Bentuk adaptasi dan tujuan adaptasi oleh beberapa hewan adalah sebagai berikut.<br><br>
1) <b>Perbedaan bentuk paruh burung sesuai dengan janis makanannya</b><br>
a. Paruh pemakan biji, contoh: paruh burung pipit.<br>
b. Paruh pencari makan di tempat becek atau lumpr, contoh: bebek.<br>
c. Paruh pemakan daging, contoh: paruh burung elang.<br>
d. Paruh pemakan serangga, contoh: paruh burung pelatuk.<br>
e. Paruh pemakan ikaan, contoh: paruh burung pelikan.<br>
f.  Paru penghisap madu, contoh: paruh burung kolibiri.<br><br>
<b>2) Perbedaan bentuk kaki hewan sesuai dengan lingkungan dab jenis makanannya</b><br>
a. Kaki burung pipit untuk bertengger.<br>
b. Kaki itik untuk berenang dalam air.<br>
c. Kaki burung pelatuk untuk memanjat.<br>
d. Kaki burung kakak tua untuk memanjat dan memegang makanan.<br>
e. Kaki ayam untuk mengais makanan.<br>
f. Kaki burung elang untuk mencengkram mangsa.<br><br>
<b>3) Penyesuaian diri tumbuhan untuk melindungi dirinya</b><br>
a. Bambu memiliki rambut-raambut halus.<br>
b. Bunga mawar dan salak memiliki duri.<br>
c. Buang durian memiliki kulit yang tebal dan berbbetuk seperti duri.<br><br> 
<b>4) Penyesuain diri tumbuhan sesuai dengan lingkungan tempat hidupnya.</b><br>
a. Tertai berdaun lebar untuk mempercepat penguapan.<br>
b. Enceng gondok memiliki batang berrongga untuk mengapung di air.<br>
c. Pohon jati dan mahoni menggugurkan daun pada musim kemarau.<br>
d. Kaktus mempunyai batang seperti gabus untuk menyimpan air.<br><br>
