<b> 1. Puisi</b><br>
Menurut ahli bahasa Rachmat Djoko Pradopo, <b>Puisi</b> adalah karangan yang terkait oleh, banyak baris dalam tiap bait, banyak kata dalam tiap baris, banyak suku kata dalam tiap baris, rima dan irama<br><br>
Unsur-unsur intrinsik pembangun puisi yang harus diperhatikan adalah sebagai berikut.<br>
<b>a. Tema</b>, yakni pokok poko persoalan yang ingindiangkat oleh penyair.<br>
<b>b. Pilihan kata</b>, berhubungan dengan kata-kata yang digunakan penyair untuk mengungkapkan perasaanya.<br>
<b>c. Gaya bahasa</b>, merupakan cara penggunaan bahasa secara khusus untuk mendapatkan efek tertentu, misalnya berbagai macam majas.<br>
<b>d. Amanat</b> yaitu pesan yang ingin disampaikan penyair kepada pembaca.<br>
Salah satu ciri puisi adalah mengandung bahasa yang bermakna konotatif. konotatif berarti mempunyai makna tautan. Makna konotatif juga disebut dengan makan tambahan.<br><br>
<b> 2. Pantun</b><br>
<b>Pantun</b> adalah salah satu bentuk puisi.Ciri-ciri pantun antara lain.<br>
a. Terdiri atas 1 bait.<br>
b. Tiap bait terdiri atas 4 baris<br>
c. Baris pertama dan kedua disebut sampiran.<br>
d. Baris ketiga dan keempat disebut isi.<br>
e. Bersajak a-b-a-b.<br>
f. Tiap baris terdiri dari atas 8 sampai 12 suku kata.<br><br>
Kita dapat mengartikan dan menjelasakan isi paantun dengan memperhatikan langkah-langkah berikut ini:<br>
a. Mengartikan kata demi kata.<br>
b. mengartikan baris ketiga dan keempat(yang merupakan isi).<br>
c. Mengabungkan artikata menjadi kalimat.<br>
d. Menyimpilkan maksud paantun.<br><br>
