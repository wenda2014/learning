<b>Contoh</b><br>
Siapa yang makan bubur itu ?<br>
--> Rani yang makan bubur itu.<br>
c.Kata tanya <b>Bagaimana</b>, memperlukan jawaban mengenai keadaan suatu hal atau cara melakukan kegiatan<br>
<b>Contoh</b><br>
Bagaimana keadaan ibu mu ?<br>
--> Kabar ibuku sehat.<br>
d. Kata tanya <b>mengapa</b>, memerlukan jawaban mengenai sebab terjadinya sesuatu.<br>
