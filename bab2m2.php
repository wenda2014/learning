Segiempat adalah bangun datar yang dibentuk dengan menghubungkan empat buah titik yang tidak segaris.<br>
Macam-Macam Segiempat :<br><br>
<b>1. Persegi(Bujur Sangkar)</b><br>
Persegi  adalah Suatu bangunan segiempat yang keempat sisinya sama panjang dan keempat sudutnya siku-siku.<br>
<img src="Image_Matematika/Persegi.png" width="300" height="200" alt=""><br> Sifat persegi :<br>
a. Mempunyai 4 sisi dan 4 titik sudut.<br>
b. Keempat sisinya sama panjang.<br>
c. Kedua diagonalnya sama panjang dan saling tegak  		lurus.<br>
d. Mempunyai 4 simetri putar.<br>
e. mempunyai 4 simetri lipat.<br><br>

<b>2. Persegi Panjang</b><br>
Persegi Panjang adalah Suatu bangun segi empat yang kedua sisi yang berhadapan sama panjang dan keempat sudutnya siku-siku<br>
<img src="Image_Matematika/Persegi_panjang.png" width="300" height="150"><br>
Sifat persegi panjang :<br>
a. Mempunyai 4 sisi dan 4 sudut.<br>
b. Mempunyai 2 pasang sisi yang sejajar.<br>
c. Sisi yang berhadapan sama panjang.<br>
d. Panjang kedua diagonalnya sama panjang dan saling membagi 2.<br>
e. Keempat sudutnya siku-siku.<br>
f. Mempunyai dua sumbu simetri(2 simetri lipat).<br>
g. Mempunyai dua simetri putar.<br><br>

<b>3. Jajargenjang</b><br>
Jajargenjang adalah segi empat dengan sisi yang behadapan sama besar dan sudut yang berhadapan juga sama besar<br>
<img src="Image_Matematika/image5.png" width="300" height="150"><br>
Sifat Janjar genjang :<br>
a. Mempunyai 4 sisi.<br>
b. Mempunyai 4 sudut.<br>
c. Mempunyai 2 pasang sisi yang sejajar.<br>
d. Mempunyai 2 sudut lancip dan 2 sudut tumpul.<br>
e. Sudut yang berhadapan besarnya sama.<br>
f. Tidak mempunyai simetri lipat.<br>
g. Mempunyai 2 simetri putar.<br><br>

<b>4. Trapesium</b><br>
Trapesium adalah Segi empat yang mempunyai sepasang sisi sejajar.<br>
<img src="Image_Matematika/image19.png" width="300" height="150"> <br>
Macam-macam Trapesium :<br><br>
<b>a. Trapesium Siku-siku</b><br>
Sifat Trapesium siku-siku :<br>
1. Mempunyai 4 sisi.<br>
2. Mempunyai 4 sudut.<br>
3. Mempunyai sepasang sisi sejajar.<br>
4. Mempunyai sepasang sisi sejajar.<br>
5. Tidak mempunyai simetri lipat.<br>
6. Mempunyai 1 simetri putar.<br><br>

<b>b. Trapesium Sama Kaki</b><br>
Sifat Trapesium sama kaki :<br>
1. Mempunyai 4 sisi.<br>
2. Mempunyai 4 sudut.<br>
3. Mempunyai sepasang sisi sejajar.<br>
4. Mempunyai sepasang sisi sama panjang.<br>
5. Mempunyai 2 pasang sudut sama besar.<br>
6. Mempunyai 1 simetri lipat.<br>
7. mempunyai 1 simetri putar.<br><br>

<b>5. Layang-Layang</b><br>
Layang-layang adalah segi empat dengan sisi yang berdekatan sama panjang dan diagonalnya saling tegak lurus.<br>
<img src="Image_Matematika/image7.png" width="300" height="150"> <br>
Sifat layang-layang:
a. Mempunyai dua pasang sisi yang sama panjang.<br>
b. Mempunyai dua diagonal yang saling berpotongan dan tegal lurus.<br>
c. Mempunyai sepasang sudut yang sama besar.<br>
d. Mempunyai satu simetri lipat.<br>
e. Mempunyai satu simetri putar.<br><br>

<b>6. Belah Ketupat</b><br>
Belah Ketupat adalah jajargenjang yang panjang sisinya sama.<br>
<img src="Image_Matematika/image5.png" width="300" height="150"><br>
Sifat-sifat belah ketupat :<br>
a. Memiliki 4 sisi.<br>
b. Keempat sisinya sama panjang.<br>
c. Sudut yang berhadapan sama besar.<br>
d. Kedua diagonalnya saling tegak lurus.<br>
e. Memiliki 2 simetri lipat.<br>
f. Memiliki 2 simetri putar.<br><br>

