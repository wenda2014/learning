<b>1. Akibat Gerakan Bumi dan Bulan</b><br><br>
a. <b>Rotasi Bumi</b><br>
<b>Rotasi Bumi</b> adalah peristiwa berputarnya bumi pada sumbunya. Waktu yang diperlukan untuk satu kali rotasi ialah 24 jam. Akibatnya dari rotasi bumi adalah sebagai berikut:<br>
1) Terjadinya siang dan malam.<br>
2) Gerak semu harian matahari.<br>
3) Perbedaan waktu.<br><br>
b. <b>Revolusi Bumi</b><br>
<b>Revolusi Bumi</b> adalah perputaran bumi mengelilingi matahari. waktu revolusi bumi adalah 1 tahun.<br>
Pengaruh revolusi bumi sebagai berikut.<br>
1) Pegantian musim.<br>
2) Gerak semu tahunan matahari.<br>
3) Penanggalan kalender Masehi.<br><br>
<b>c. Rotasi dan Revolusi Bulan</b><br>
<b>Rotasi bulan</b> adalah perputaran bulan pada porosnya. Revolusi bulan terhadap bumi adalah gerakan bulan mengelilingi bumi. Waktu yang dibutuhkan bulan untuk melakukan satu kali revolusi 29,5 hari. Akibat revolusi bulan adalah terjadi pasang surut air laut. gerahana matahari, gerahana bulan.<br><br>
<b>1. Gerahana Bulan dan Matahari</b><br><br>
<b>a. Gerahana Matahari</b><br>
Gerahana matahari terjadi ketika bulan berada di antara bumi dan matahari dalam satu garis lurus sehingga sebagian sinar matahari ke permukaan bumi tertutup oleh bulan.<br><br>
<b>Gerahana matahari ada tiga jenis, yaitu:</b> <br>
<b>1) Gerahana matahari total,</b> terjadi jika permukaan bumi tertutup oleh bayang-bayang umbra bulan.<br>
<b>2) Gerahana matahari sebagain, </b>terjadi jika permukaan bumi tertutup penumbra bulan.<br>
<b>3) Gerahana matahari cincin</b>, terjadi jika bulan berada pada titik terjauh dari bumi.<br><br>
<b>b. Gerahana Bulan</b><br>
Gerahana bulan terjadi saat bumi berada di antara matahari dan bulan pada satu garis lurus yang sama, sehingga sinar matahari tidak dapat mencapai bulan karena terhalang oleh bumi.<br><br>
<b>Ada tiga jenis gerahan bulan, yaitu</b><br>
<b>1) Gerahan bulan total</b>, terjadi jika seluruh lingkaran bulan memasuki daerah bayangan umbra.<br>
<b>2) Gerahana bulan sebagian</b>, terjadi jika sebagaian permukaan bulan mulai memasuki daerah bayangan umbra.<br>
<b>3) Gerahana bulan penumbra</b>, terjadi jika bulan memasuki daerah bayangan penumbra.<br>
