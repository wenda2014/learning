<b>1. Mata (Indra pengelihat) </b><br><br>
<img src="Image_Ipa/mata.png" width="200" height="250"> <br>
Bagian-bagian mata dan fungsinya adalah sebagai berikut:<br>
a.<b> Kornea</b> adalah bagian yang bening dan transparan yang berfungsi untuk menerima rangsangan cahaya dan meneruskan kebagiab dalam.<br>
b.<b>Iris</b> adalah bagian mata yang berfungsi mangtur banyak sedikit cahaya yang masuk kemata, iris memiliki warna berbeda.<br>
c. <b>Pupil</b> adalah bulatan hitam ditengah iris yang berfungsi sebagai pintu masuk cahaya.<br>
d. <b>Lensa mata</b> berfungsi untuk meneruskan dan memfokuskan cahaya agar bayangan jatuh tepat diretina.<br>
e. <b>Retina</b> berfungsi membentuk bayangan benda dan kemudian mengirimnya ke otak.<br>
f. <b>Saraf mata</b>  berfungsi untuk meneruskan rangsangan otak.<br>
g. <b>Otot mata</b> berfungsi untuk menggerakan bola mata.<br>
h. <b>Sklera</b> berfungsi untuk melindungi bola mata.<br><br>
<b>2. Telinga (Indra Pendengaran)</b><br><br>
<img src="Image_Ipa/telinga3.jpg" width="253" height="192"><br><br>
<b>Bagian-bagian Telinga</b><br>
a. <b>Telinga Luar</b>, terdiri dari daun telinga, lubang telinga, dan liang pendengaran. Bagian ini berfungsi untuk menerima dan mengumpulkan gelombang bunyi.<br>
b. <b>Telinga Bagian Tengah</b>, teridiri dari:<br>
1) Selaput gendang berfungsi untuk menerima bunyi dari telinga luar dan meneruskan kebagian dalam.<br>
2) Tulang-tulang pendenganran yang teridir dari tulang martil, tulang landasan, dan tulang sanggurdi. berfungsi meneruskan getaran bunyi menuju koklea.<br>
3. Saluran Eustachius berfungsi menghubungkan rongga mulut dan telinga tengah, serta mengatur keseimbangan antara tekanan udar diluar dan di dalam telinga.<br>
<b>c. Telinga bagain dalam</b>, terdiiri dari:<br>
1) Tiga saluran setengah lingkaran, berfungsi mengendalikan keseimbangan tubuh.<br>
2) Tingkap Jorong, tingkap bundar, dan koklea. Di dalam koklea terdapat organ korti yang berfungsi mengubah suara menjadi implus menju otak.<br><br>
<b>3. Lidah ( Indra Pengecap)</b><br><br>
<img src="Image_Ipa/lidah1.jpg" width="180" height="260"><br>
<b>Bagian-bagian lidah yang paling peka terhadap rasa</b><br>
Permukaan lidah memiliki tonjolan yang disebut papila. Di dalam papila terdapat banyak tunas pengecap atau taste bud yang dapat mengenali rasa.<br><br>
<b>4. Hidung Indra Pembau</b><br><br>
<img src="Image_Ipa/hidung.png" width="209" height="170"><br><br>
<b> Bagian hidung</b><br>
Bagian-bagian hidung dan fungsinya adalah sebagai berikut.<br>
a. <b>Lubang Hidung</b>, berfungsi sebagai tempat penerima dan pengumpul udara yang pertama kali.<br>
b. <b>Rongga hidung</b>, terdapat rambut hidung yang berfungsi sebagai penyaring udara yang masuk.<br> 
c. <b>Serabut saraf (saraf olfaktori)</b>, berfungsi menerima bau dan meneruskan implus atau rangsang ke otak.<br><br>
<b>4. Kulit (indra peraba)</b><br><br>
<img src="Image_Ipa/bagian_kulit.jpg" width="238" height="178"> <br>
<b>Bagian-bagian kulit</b><br>
Bagian-bagian kulit dan fungsinya adalah sebagai berikut.<br>
a. <b>Kulit ari (lapisan epidermis)</b>, berfungsi melindungi bagian dalam kulit  dan mencegah penguapan air dari dalam tubuh.<br>
b. <b>Kelenjar Keringat</b>, berfungsi menghasilkan keringat dan mengatur suhu tubuh.<br>
c. <b>Lapisan Lemak</b>, berfungsi menghangatkan tubuh.<br>
d. <b>Otot penggerak rambut</b>, berfungsi untuk mengatur gerakan rambut.<br>
e. <b>Pembuluh darah</b>, berfungsi mengalirkan darah. Darah membawa nutrisi penting untuk kulit.<br>


