<b>1. Gaya</b><br>
<b>Gaya</b> adalah tarikan atau dorongan. Dengan adanya gaya, benda dapat mengalami perubahan bentuk, arah gerak, dan gerak benda.<br><br>
<b>A. Macam-Macam Gaya :</b><br>
<b>a. Gaya Gravitasi Bumi </b><br>
Gaya gravitasi bumi sering disebut gaya tarik bumi. Gaya ini menyebabkan bend tetap menempel dibumi.<br>
Contoh:<br>
Buah apel jatuh dari pohon.<br>
<b>b. Gaya Gesek</b><br>
Gaya yang menimbulkan hambatan ketika dua bersentuhan.<br>
<b>c. Gaya Magnet</b><br>
Gaya magnet adalah gaya yang dimiliki oleh magnet yang dapat menarik benda-benda  yang didekatkan pada magnet itu.<br>
Contoh:<br>
Gaya magnet pintu pada kulkas, peniti akan menempel bil didekatkan ke magnet.<br>
<b>d. Gaya Pegas</b><br>
Gaya pegas adalah gaya yang ditimbulkan oleh benda yang menyerupai pegas.<br>
Contoh:<br>
Ketapel karet yang kita tarik teregangya karet yang selanjutnya kita lepaskan akan menimbulkan gaya pegas.<br>
<b>e. Gaya Listrik Statis</b><br>
Gaya Listrik Status adalah suatu tarikan atau dorongan karena arus listrik/aliran listrik.<br>
Contoh:<br>
Penggaris atau sisir plastik digosok-gosok pada rambut agak lama, kemudian didekatkan pada sobekan kertas, maka sobekan kertas akan tertarik dan menempel oleh penggaaris, karena penggaris dan sisir setelah digesekan ke rambut berulang kali tadi bermuatan listrik.<br>
<b>f. Gaya Otot</b><br>
Gaya yang ditimbulkan oleh manusia atau hewan.<br>
<b>g. Gaya Tarik</b><br>
Gaya tarik adalah gaya yang ditimbulkan oleh tarik suatu benda atau makluk hidup.<br>
<b>h. Gaya Dorong</b><br>
Gaya dorong adalah gaya yang ditimbulkan oleh dorongan suatu benda atau makhluk hidup<br><br>
<b>Sifat-Sifat Gaya: </b><br>
1) Gaya dapat mengubah bentuk suatu benda.<br>
Contoh :<br>
- Tanah liat yang bulat jika ditekan, maka akan berubah menjadi pipih.<br>
2) Gaya dapat mengubah arah gerak suatu benda.<br>
Contoh:<br>
- Pada saat kita membuka atau menutup pintu.<br>
3) Gaya dapat membuat benda yang diam menjadi bergerak ataupun sebaliknya.<br>
- Bola yang menggelindungi dapat berhenti bila kita tahan dengan kaki.<br><br>
<b>2. Cahaya</b><br>
<b>Cahaya</b> adalah pancaran elektomagnetik yang terlihat oleh matatelanjang mata manusia.<br><br>
<b>Sifat-sifat cahaya</b><br>
Sifat-sifat cahaya antara lain:<br>
1) Cahaya merambat lurus<br>
Sifat cahaya yang dapat merambat lurus banyak dimanfaatkan pada lampu kendaraan bermotor dan lampu senter.<br>
2) Cahaya dapat menembus benda bening.<br>
Cahaya dapat masuk kedalam rumah salah satunya karena cahaya.<br>
3) Cahaya dapat dipantulkan.<br>
Cahaya yang mengenai permukaan yang licin seperti cermin datar akan dipantulkan.<br>
4) Cahaya dapat dibiasakan<br>
<b>Pembiasan</b> merupakan peristiwa pembelokan cahaya setelah melewati medium dengan kerapatan berbeda.<br>
5) Cahaya dapat diuraikan<br>
Peristiwa penguraian cahaya disebut <b>dispersi</b>.Contoh: terjadinya pelangi yang berwarna-warni.<br><br>

